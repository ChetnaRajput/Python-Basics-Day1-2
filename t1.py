# print simple message "Hello! Welcomee to Python work."
message = "Hello! Welcome to Python work."
print(message)

# Sliceing

print("accessing first five latters of the string ([0:6])", message[0:6])
print("accessing last five latters of the string(negative indexing)([-5::])", message[-5::])
print("accessing reverse string(negative indexing)([::-1])", message[::-1])
print("accessing some part of the string([7:25])", message[7:25])

