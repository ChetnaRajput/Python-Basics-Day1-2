# Create a dictionary as a simple string and print it.
details = {"Name":"Adam",
           "Designation":"Manager",
           "Contact":9514786320}
print("\nDictionary- \n", details)

# Print only Keys of dictionary
print("\nKeys of Dictionary-")
for keys in details:
    print(keys)

print("\nValues of Dictionary-")
for values in details:
    print(details[values])


